/**
 * Regenerates docs/operations/frontend-route-inventory.md from App Router page.tsx files.
 * Run: npm run inventory:routes
 */
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const appRoot = join(process.cwd(), "src", "app");
const outFile = join(process.cwd(), "..", "docs", "operations", "frontend-route-inventory.md");

function walk(dir, acc) {
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      walk(full, acc);
      continue;
    }
    if (entry !== "page.tsx") continue;

    const rel = relative(appRoot, full).replace(/\\/g, "/");
    const route = rel
      .replace(/\/page\.tsx$/, "")
      .split("/")
      .filter((part) => part && !part.startsWith("(") && !part.endsWith(")"))
      .join("/");

    const normalized = `/${route}`.replace(/\/+/g, "/");
    acc.push(normalized);
  }
}

function familyForRoute(route) {
  if (route.startsWith("/api")) return "API routes";
  if (route.startsWith("/admin")) return "Admin dashboard";
  if (route.startsWith("/cashier")) return "Cashier dashboard";
  if (route.startsWith("/customer")) return "Customer dashboard";
  if (route.startsWith("/partner")) return "Partner dashboard";
  if (route.startsWith("/vendor")) return "Vendor dashboard";
  if (
    [
      "/login",
      "/register",
      "/logout",
      "/forgot-password",
      "/reset-password",
      "/unauthorized",
    ].includes(route)
  ) {
    return "Auth & session";
  }
  return "Public & cross-role entry";
}

const routes = [];
walk(appRoot, routes);
routes.sort();

const byFamily = new Map();
for (const r of routes) {
  const fam = familyForRoute(r);
  if (!byFamily.has(fam)) byFamily.set(fam, []);
  byFamily.get(fam).push(r);
}

const generated = new Date().toISOString().slice(0, 19) + "Z";
let md = `# Frontend route inventory\n\n`;
md += `> Auto-generated by \`npm run inventory:routes\` (${generated}). Do not hand-edit enumeration; re-run the script after adding pages.\n\n`;
md += `## Summary\n\n`;
md += `- **Total Next.js page routes:** ${routes.length}\n`;
md += `- **Collision check:** run \`npm run check:routes\` (expects zero collisions; compatibility stubs listed there).\n\n`;

for (const [family, list] of [...byFamily.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
  md += `## ${family} (${list.length})\n\n`;
  for (const path of list) {
    md += `- \`${path}\`\n`;
  }
  md += `\n`;
}

writeFileSync(outFile, md, "utf8");
console.log(`Wrote ${relative(process.cwd(), outFile)} (${routes.length} routes).`);
